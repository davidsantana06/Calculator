class Calculator:
    def sum_numbers(self, x, y):
        return (x + y)

    def subtract_numbers(self, x, y):
        return (x - y)

    def multiply_numbers(self, x, y):
        return (x * y)

    def divide_numbers(self, x, y):
        return (x / y)

    def power_number(self, x, y):
        return (x ** y)